                          `Telepon: ${phone}%0A` +
                          `Email: ${email}%0A` +
                          `Layanan: ${service}%0A%0A` +
                          `Pesan: ${message}`;
    
    // WhatsApp number (replace with actual number)
    const whatsappNumber = '6281234567890';
    
    // Open WhatsApp
    window.open(`https://wa.me/${whatsappNumber}?text=${whatsappMessage}`, '_blank');
    
    // Reset form
    contactForm.reset();
    
